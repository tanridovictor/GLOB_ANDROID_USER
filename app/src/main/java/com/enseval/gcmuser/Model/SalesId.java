package com.enseval.gcmuser.Model;

public class SalesId {
    private int id;
    private int id_sales;

    public SalesId(int id, int id_sales) {
        this.id = id;
        this.id_sales = id_sales;
    }

    public int getId() {
        return id;
    }

    public int getId_sales() {
        return id_sales;
    }
}
